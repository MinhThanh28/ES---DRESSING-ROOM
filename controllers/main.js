  function addProductToTab(items, tabId) {
    const container = document.getElementById(tabId);
    container.innerHTML = '';
    items.forEach((item) => {
      const html = `
          <div class="card">
              <img
                  src="${item.imageShowinTab}"
                  alt="Tên ảnh"
                  class="card-img-top"
                  style="width: 200px; height: 250px"
              />
              <div class="card-body">
                  <h5
                      class="card-title"
                      style="font-size: 15px; text-align: center"
                  >
                      ${item.name}
                  </h5>
                  <button class="btn btn-success" onclick='tryClothes(${JSON.stringify(
                    item
                  )})'>Thử đồ</button>
              </div>
          </div>
      `;
      container.innerHTML += html;
    });
  }
  function tryClothes(item) {
    const bikiniTop = document.querySelector(`.${item["class"]}`);
    bikiniTop.innerHTML = `<img style="position: absolute; width: 250px; height: 400px;" src="${item.image}" alt="">`;
  }
  
